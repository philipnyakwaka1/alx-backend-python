#!/usr/bin/env python3
"""
This module provides a type-annotated function to concatenate two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    Return a concatenated string
    Args:
        str1 (str): The first string to be concatenated
        str2 (str): The second string to be concatenated
    Returns:
        str: Concatenation of str1 and str2
    """

    return str1 + str2
