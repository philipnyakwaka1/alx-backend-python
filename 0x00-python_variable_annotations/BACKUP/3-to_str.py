#!/usr/bin/env python3

"""
This module contains a type-annotated function to_str that takes
a float n as argument and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Turns float to string
    Args:
        n (float): number to be turned to string
    Return:
        string representation of n
    """
    return str(n)
