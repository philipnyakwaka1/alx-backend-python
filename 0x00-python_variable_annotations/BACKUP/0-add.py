#!/usr/bin/env python3

"""
This module contains a type-annotated function add that
takes a float a and a float b as arguments and returns their
sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floats
    Args:
        a (float): first number to be added
        b (float): second number to be added
    Return:
        sum of a and b
    """
    return a + b
