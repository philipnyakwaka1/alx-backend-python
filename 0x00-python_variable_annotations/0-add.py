#!/usr/bin/env python3
"""
This module contains a type-annotated function add that adds takes
two floats as arguments float a and b and returns their sum as float
"""


def add(a: float, b: float) -> float:
    """
    Return the sum of two float numbers
    Args:
        a (float): The first number to be added
        b (float): The second number to be added
    Returns:
        float: The sum of a and b
    """

    return a + b
