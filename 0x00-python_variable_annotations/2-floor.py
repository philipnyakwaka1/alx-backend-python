#!/usr/bin/env python3
import math

"""
This module contains a pe-annotated function
floor which takes a float n as argument and
returns the floor of the float.
"""


def floor(n: float) -> float:
    """
    Adds two floats
    Args:
        n (float): number to be floored
    Return:
        floor of n
    """
    return math.floor(n)